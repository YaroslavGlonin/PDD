package com.yaroslavsoftware.pdd;

public class WorldPopulation {
	private int image;
	private String text;
 
	public WorldPopulation(int image, String text) {
		this.image = image;
		this.text = text;
	}
 
	public String gettext() {
		return this.text;
	}
 
	public int getImg() {
		return this.image;
	}
 
}